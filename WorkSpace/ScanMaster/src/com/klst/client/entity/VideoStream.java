package com.klst.client.entity;

public class VideoStream {
	private int index;
	private VideoProtocol videoProtocol;
	private VideoFormat resolution;
	private String callRate;
	private String frameRate;
	
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public VideoProtocol getVideoProtocol() {
		return videoProtocol;
	}
	public void setVideoProtocol(VideoProtocol videoProtocol) {
		this.videoProtocol = videoProtocol;
	}
	
	public VideoFormat getResolution() {
		return resolution;
	}
	public void setResolution(VideoFormat resolution) {
		this.resolution = resolution;
	}
	public String getCallRate() {
		return callRate;
	}
	public void setCallRate(String callRate) {
		this.callRate = callRate;
	}
	public String getFrameRate() {
		return frameRate;
	}
	public void setFrameRate(String frameRate) {
		this.frameRate = frameRate;
	}
	
	
}
